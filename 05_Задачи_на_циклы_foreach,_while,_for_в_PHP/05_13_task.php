<?php

/*
  Дан массив числами, например: ['10', '20', '30', '50', '235', '3000'].
 * Выведите на экран только те числа из массива, которые начинаются на 
 * цифру 1, 2 или 5. 
 */

$arr = ['10', '20', '30', '50', '235', '3000'];

echo '<pre>';
print_r($arr);
echo '</pre>';


foreach ($arr as $value) {

    $param = (string) $value;


    if ($param[0] == 1 || $param[0] == 2 || $param[0] == 5) {

        echo $value . '<br />';
    }
}